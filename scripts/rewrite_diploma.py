BACKUP_PATH = DOC_PATH.with_name(DOC_PATH.stem + " - backup.docx")


def ensure_backup() -> None:
    if not BACKUP_PATH.exists():
        shutil.copy2(DOC_PATH, BACKUP_PATH)


def set_paragraph_text(paragraph, text: str) -> None:
    if paragraph.runs:
        paragraph.runs[0].text = text
        for run in paragraph.runs[1:]:
            run.text = ""
    else:
        paragraph.add_run(text)


def fill_range(doc: Document, start: int, end: int, texts: list[str]) -> None:
    for offset, index in enumerate(range(start, end + 1)):
        text = texts[offset] if offset < len(texts) else ""
        set_paragraph_text(doc.paragraphs[index], text)


def set_table_rows(table, rows: list[list[str]]) -> None:
    for r_index, row_values in enumerate(rows[: len(table.rows)]):
        row = table.rows[r_index]
        for c_index, value in enumerate(row_values[: len(row.cells)]):
            cell = row.cells[c_index]
            if cell.paragraphs:
                set_paragraph_text(cell.paragraphs[0], value)
                for extra in cell.paragraphs[1:]:
                    set_paragraph_text(extra, "")
            else:
                cell.text = value
    for r_index in range(len(rows), len(table.rows)):
        row = table.rows[r_index]
        for cell in row.cells:
            if cell.paragraphs:
                set_paragraph_text(cell.paragraphs[0], "")
                for extra in cell.paragraphs[1:]:
                    set_paragraph_text(extra, "")
            else:
                cell.text = ""


def non_empty_lines_from_file(path: Path) -> list[str]:
    lines = path.read_text(encoding="utf-8").splitlines()
    return [line.rstrip() for line in lines if line.strip()]


def java_method_excerpt(path: Path) -> list[str]:
    lines = path.read_text(encoding="utf-8").splitlines()
    start = 0
    for i, line in enumerate(lines):
        if "public List<RecommendationResponseDTO> getPersonalizedRecipeRecommendations(" in line:
            start = i
            break
    excerpt = []
    for line in lines[start:start + 20]:
        if line.strip():
            excerpt.append(line.rstrip())
    return excerpt


