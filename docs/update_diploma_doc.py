def refresh_workdir() -> None:
    if WORK_DIR.exists():
        shutil.rmtree(WORK_DIR)
    source_path = SOURCE_DOCX_PATH if SOURCE_DOCX_PATH.exists() else DOCX_PATH
    with zipfile.ZipFile(source_path, "r") as archive:
        archive.extractall(WORK_DIR)


def repack_docx() -> None:
    with zipfile.ZipFile(DOCX_PATH, "w", zipfile.ZIP_DEFLATED) as archive:
        for file_path in sorted(WORK_DIR.rglob("*")):
            if file_path.is_file():
                archive.write(file_path, file_path.relative_to(WORK_DIR))


