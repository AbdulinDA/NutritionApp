def get_paragraph_text(paragraph: etree._Element) -> str:
    return "".join(paragraph.xpath(".//w:t/text()", namespaces=NS)).strip()


def iter_body_paragraphs(root: etree._Element) -> list[etree._Element]:
    return root.xpath(".//w:body/w:p", namespaces=NS)


def clear_paragraph_runs(paragraph: etree._Element) -> None:
    for child in list(paragraph):
        if child.tag != f"{W}pPr":
            paragraph.remove(child)


def set_paragraph_text(paragraph: etree._Element, text: str) -> None:
    clear_paragraph_runs(paragraph)
    run = etree.Element(f"{W}r")
    text_el = etree.SubElement(run, f"{W}t")
    if text.startswith(" ") or text.endswith(" ") or "  " in text:
        text_el.set(f"{{{XML_NS}}}space", "preserve")
    text_el.text = text
    paragraph.append(run)


def clone_with_text(template_paragraph: etree._Element, text: str) -> etree._Element:
    new_paragraph = copy.deepcopy(template_paragraph)
    set_paragraph_text(new_paragraph, text)
    return new_paragraph


def find_paragraph(paragraphs: list[etree._Element], text: str) -> etree._Element:
    for paragraph in paragraphs:
        if get_paragraph_text(paragraph) == text:
            return paragraph
    raise ValueError(f"Paragraph not found: {text}")


def find_paragraph_containing(paragraphs: list[etree._Element], fragment: str) -> etree._Element:
    for paragraph in paragraphs:
        if fragment in get_paragraph_text(paragraph):
            return paragraph
    raise ValueError(f"Paragraph containing fragment not found: {fragment}")


def insert_before(reference: etree._Element, new_paragraph: etree._Element) -> None:
    parent = reference.getparent()
    parent.insert(parent.index(reference), new_paragraph)


def insert_after(reference: etree._Element, new_paragraph: etree._Element) -> None:
    parent = reference.getparent()
    parent.insert(parent.index(reference) + 1, new_paragraph)


def remove_paragraph(paragraph: etree._Element) -> None:
    parent = paragraph.getparent()
    parent.remove(paragraph)


def remap_citations(text: str) -> str:
    def replace(match: re.Match[str]) -> str:
        raw = match.group(1)
        parts = [part.strip() for part in raw.split(",")]
        remapped: list[str] = []
        for part in parts:
            if not part.isdigit():
                return match.group(0)
            number = int(part)
            remapped.append(str(OLD_TO_NEW_CITATIONS.get(number, number)))
        return "[" + ", ".join(remapped) + "]"

    return re.sub(r"\[([0-9,\s]+)\]", replace, text)


def merge_into_first(paragraphs: list[etree._Element], texts: list[str], merged_text: str) -> None:
    first = find_paragraph(paragraphs, texts[0])
    set_paragraph_text(first, merged_text)
    for text in texts[1:]:
        paragraph = find_paragraph(paragraphs, text)
        remove_paragraph(paragraph)


def merge_into_first_containing(paragraphs: list[etree._Element], fragments: list[str], merged_text: str) -> None:
    first = find_paragraph_containing(paragraphs, fragments[0])
    set_paragraph_text(first, merged_text)
    for fragment in fragments[1:]:
        paragraph = find_paragraph_containing(iter_body_paragraphs(first.getroottree().getroot()), fragment)
        remove_paragraph(paragraph)


